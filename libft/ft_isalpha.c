#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	a;

	a = c;
	if (c < 0 || c > UCHAR_MAX)
		return (0);
	if ((a >= 65 && a <= 90) || (a >= 97 && a <= 122))
		return (1);
	return (0);
}
